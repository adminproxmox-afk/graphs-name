function renderContent(section) {
  if (section.type === 'list') {
    return `<ul>${section.items.map(item => `<li>${item}</li>`).join('')}</ul>`;
  }

  if (section.type === 'grid') {
    return `<div class="practical-grid">${section.items.map(item => `
      <div class="practical-card">
        <h3>${item.title}</h3>
        <p>${item.text}</p>
      </div>
    `).join('')}</div>`;
  }

  return `<p>${section.content}</p>`;
}

function renderPracticalPage() {
  const id = document.body?.dataset.practicalId;
  const page = PRACTICAL_PAGES[id];
  const root = document.getElementById('practical-root');
  if (!page || !root) return;

  document.title = `Graphly - ${page.title}`;
  root.innerHTML = `
    <section class="page-hero">
      <div>
        <p class="page-kicker">Практика</p>
        <h1 class="page-title">${page.title}</h1>
        <p class="page-lead">${page.intro}</p>
      </div>
      <aside class="page-stat">
        <strong>${page.title.match(/\d+/)?.[0] || '•'}</strong>
        ${page.stat}
      </aside>
    </section>

    <article class="practical-article">
      <div class="practical-overline">${page.subtitle}</div>
      <div class="practical-card">
        ${page.sections.map(section => `
          <section>
            <h2>${section.title}</h2>
            ${renderContent(section)}
          </section>
        `).join('')}
      </div>
      <div class="practical-callout">${page.note}</div>
      <nav class="practical-nav" aria-label="Навігація практичними роботами">
        ${page.prev ? `<a class="btn-secondary" href="practical_work_${page.prev.split('-')[1]}.html"><i class="ri-arrow-left-line"></i> Попередня</a>` : '<span></span>'}
        ${page.next ? `<a class="btn-primary" href="practical_work_${page.next.split('-')[1]}.html">Наступна <i class="ri-arrow-right-line"></i></a>` : '<span></span>'}
      </nav>
    </article>
  `;
}

document.addEventListener('DOMContentLoaded', renderPracticalPage);
